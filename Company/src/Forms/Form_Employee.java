    private void Btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_AddActionPerformed

        Set_Values();
        emp.Add();
         
        // Add phones
        phones.setEmp_NO(Integer.parseInt(Txt_EmpNO.getText()));
        for(int x=0; x<Table_Contact.getRowCount() ; x++)
        {
            phones.setPhone(Table_Contact.getValueAt(x, 0).toString());
            phones.Add();
        }
        
        Clear_Data();
        emp.Get_All_Rows(Table_Emp);
        
    }//GEN-LAST:event_Btn_AddActionPerformed

    private void Btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_UpdateActionPerformed

        Set_Values();
        emp.Update();
        
        // Phones
        phones.setEmp_NO(Integer.parseInt(Txt_EmpNO.getText()));
        phones.Delete_By_emp(); // supprimer les anciens contacts pour faire un Update 
        
        // Add news contact
        for(int x=0; x<Table_Contact.getRowCount() ; x++)
        {
            phones.setPhone(Table_Contact.getValueAt(x, 0).toString());
            phones.Add();
        }
        
        
        Clear_Data();
        emp.Get_All_Rows(Table_Emp);
      
    }//GEN-LAST:event_Btn_UpdateActionPerformed

    private void Btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_DeleteActionPerformed
        
        if(Tools.Confirm_Msg(" Voulez vous vraiment supprimer l'employé ?! "))
        {
        
            // supprimer les Phones avant de supprimer l employe
            phones.setEmp_NO(Integer.parseInt(Txt_EmpNO.getText()));
            phones.Delete_By_emp(); // supprimer
            
            // suppression employe
            Set_Values();
            emp.Delete();
            
            Clear_Data();
            emp.Get_All_Rows(Table_Emp);
        }
        
    }//GEN-LAST:event_Btn_DeleteActionPerformed

