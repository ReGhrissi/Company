    private void Table_EmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_EmpMouseClicked
        try
        {
             int row = Table_Emp.getSelectedRow();
            
            String Str_Number =Table_Emp.getValueAt(row, 0).toString();
            String Str_Name =Table_Emp.getValueAt(row, 1).toString();
            String Str_Address =Table_Emp.getValueAt(row, 2).toString();
            String Str_Salary =Table_Emp.getValueAt(row, 3).toString();
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date Hi_Date = sdf.parse(Table_Emp.getValueAt(row, 4).toString() );
            Date Br_Date = sdf.parse(Table_Emp.getValueAt(row, 5).toString() );
            
           // String Str_Dept = Table_Emp.getValueAt(row, 6).toString();
            
            Txt_EmpNO.setText(Str_Number);
            Txt_EmpName.setText(Str_Name);
            Txt_EmpAddress.setText(Str_Address);
            Txt_EmpSalary.setText(Str_Salary);
            
            JDate_Hiring.setDate(Hi_Date);
            JDate_Birth.setDate(Br_Date);
            
            Combo_Dept.setSelectedItem(Table_Emp.getValueAt(row, 7)); // '7' ? : apres la selection d'une ligne du tableau 
                                                                     //  On veux afficher sur le combo le nom du 
                                                                    // departement et non pas son ID
            //affichage des contacts de l employe sur le tableau de contacts apres une selection par la souris 
            // sur le tableau Table_Emp
            phones.setEmp_NO(Integer.parseInt(Txt_EmpNO.getText()));
            phones.Get_All_Rows(Table_Contact);
            
                                                                  
            Btn_Add.setEnabled(false);
            Btn_Update.setEnabled(true);
            Btn_Delete.setEnabled(true);
            
            
        }
        catch(Exception ex)
        {
            Tools.MsgBox(ex.getMessage());
        }
        
    }//GEN-LAST:event_Table_EmpMouseClicked

