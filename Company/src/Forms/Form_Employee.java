    private String Get_My_LikeQuery()
    {
        
        return " like'%" + Txt_Search.getText() +  "%'" ;
    }
    
    private void Btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_SearchActionPerformed

        String Str_Search = "select * from employee_data where " ;

        if(Rdo_Number.isSelected())
        {
            Str_Search += "Number " + Get_My_LikeQuery();
        }
        else if(Rdo_Name.isSelected())
        {
            Str_Search += "Name " + Get_My_LikeQuery() ;
        }
        else if(Rdo_Address.isSelected())
        {
            Str_Search += "Address " + Get_My_LikeQuery() ;
        }
        else if(Rdo_Salary.isSelected())
        {
            Str_Search += "Salary " + Get_My_LikeQuery() ;
        }
        else if(Rdo_Hiring.isSelected())
        {
            Str_Search += "Hiring_Date " + Get_My_LikeQuery() ;
        }
        else if(Rdo_Birth.isSelected())
        {
            Str_Search += "Date_Of_Birth " + Get_My_LikeQuery() ;
        }
        else if(Rdo_Dept.isSelected())
        {
            Str_Search += "Departement_NO "  + Get_My_LikeQuery() +
                            " or Departement " + Get_My_LikeQuery() ;
        }
        else
        {
            String STR_EMPNO =phones.Get_EmpNO_By_Phone(Txt_Search.getText());
            
            Str_Search += " Number like '%"+ STR_EMPNO +"%' ;" ;
        }

        emp.Get_Custom_Rows(Str_Search, Table_Emp);
        
    }//GEN-LAST:event_Btn_SearchActionPerformed

