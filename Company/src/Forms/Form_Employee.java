        private void Set_Values()
    {
        emp.setEmp_NO(Integer.parseInt(Txt_EmpNO.getText()));
        emp.setEmp_Name(Txt_EmpName.getText());
        emp.setAddress(Txt_EmpAddress.getText());
        emp.setSalary(Double.parseDouble(Txt_EmpSalary.getText()));
        
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        String H_Date = String.valueOf(f.format(JDate_Hiring.getDate()));
        String B_Date = String.valueOf(f.format(JDate_Birth.getDate()));
        
        emp.setHiring_Date(H_Date);
        emp.setBirth_Date(B_Date);
        
        String D_Name = Combo_Dept.getSelectedItem().toString(); // cherchons Dept_NO on donnant le nom du departement
        String D_NO = dept2.GetValue_By_Name(D_Name);
        
        emp.setDept_NO(Integer.parseInt(D_NO));
    }
    
    
   
    
